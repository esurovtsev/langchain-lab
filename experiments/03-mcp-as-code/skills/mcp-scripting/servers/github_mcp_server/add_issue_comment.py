async def add_issue_comment(
    session,
    body: str,
    issue_number: float,
    owner: str,
    repo: str,
    output_file: str | None = None,
):
    """
    Add a comment to a specific issue in a GitHub repository. Use this tool to add comments
    to pull requests as well (in this case pass pull request number as issue_number), but
    only if user is not asking specifically to add review comments.

    Args:
        session: MCP ClientSession from connect().
        body (str): Comment content
        issue_number (float): Issue number to comment on
        owner (str): Repository owner
        repo (str): Repository name
        output_file (str | None): If set, write full result to this file and return a summary.

    Returns:
        str: Tool result text. If output_file is set, returns a short summary instead.
    """
    args = {}
    args["body"] = body
    args["issue_number"] = issue_number
    args["owner"] = owner
    args["repo"] = repo

    result = await session.call_tool("add_issue_comment", arguments=args)

    # Extract text from result content
    texts = []
    for content in result.content:
        if hasattr(content, "text"):
            texts.append(content.text)
        else:
            texts.append(str(content))
    full_text = "\n".join(texts)

    if output_file:
        with open(output_file, "w") as f:
            f.write(full_text)
        lines = full_text.count("\n")
        return f"Wrote {len(full_text)} chars ({lines} lines) to {output_file}"

    return full_text
