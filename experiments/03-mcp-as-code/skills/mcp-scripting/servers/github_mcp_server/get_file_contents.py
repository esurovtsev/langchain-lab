async def get_file_contents(
    session,
    owner: str,
    repo: str,
    path: str = '/',
    ref: str | None = None,
    sha: str | None = None,
    output_file: str | None = None,
):
    """
    Get the contents of a file or directory from a GitHub repository

    Args:
        session: MCP ClientSession from connect().
        owner (str): Repository owner (username or organization)
        repo (str): Repository name
        path (str): Path to file/directory
        ref (str): Accepts optional git refs such as `refs/tags/{tag}`, `refs/heads/{branch}` or `refs/pull/{pr_number}/head`
        sha (str): Accepts optional commit SHA. If specified, it will be used instead of ref
        output_file (str | None): If set, write full result to this file and return a summary.

    Returns:
        str: Tool result text. If output_file is set, returns a short summary instead.
    """
    args = {}
    args["owner"] = owner
    args["repo"] = repo
    if path is not None: args["path"] = path
    if ref is not None: args["ref"] = ref
    if sha is not None: args["sha"] = sha

    result = await session.call_tool("get_file_contents", arguments=args)

    # Extract text from result content
    texts = []
    for content in result.content:
        if hasattr(content, "text"):
            texts.append(content.text)
        else:
            texts.append(str(content))
    full_text = "\n".join(texts)

    if output_file:
        with open(output_file, "w") as f:
            f.write(full_text)
        lines = full_text.count("\n")
        return f"Wrote {len(full_text)} chars ({lines} lines) to {output_file}"

    return full_text
