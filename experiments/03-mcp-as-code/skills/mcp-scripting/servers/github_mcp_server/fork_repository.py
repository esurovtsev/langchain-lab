async def fork_repository(
    session,
    owner: str,
    repo: str,
    organization: str | None = None,
    output_file: str | None = None,
):
    """
    Fork a GitHub repository to your account or specified organization

    Args:
        session: MCP ClientSession from connect().
        owner (str): Repository owner
        repo (str): Repository name
        organization (str): Organization to fork to
        output_file (str | None): If set, write full result to this file and return a summary.

    Returns:
        str: Tool result text. If output_file is set, returns a short summary instead.
    """
    args = {}
    args["owner"] = owner
    args["repo"] = repo
    if organization is not None: args["organization"] = organization

    result = await session.call_tool("fork_repository", arguments=args)

    # Extract text from result content
    texts = []
    for content in result.content:
        if hasattr(content, "text"):
            texts.append(content.text)
        else:
            texts.append(str(content))
    full_text = "\n".join(texts)

    if output_file:
        with open(output_file, "w") as f:
            f.write(full_text)
        lines = full_text.count("\n")
        return f"Wrote {len(full_text)} chars ({lines} lines) to {output_file}"

    return full_text
