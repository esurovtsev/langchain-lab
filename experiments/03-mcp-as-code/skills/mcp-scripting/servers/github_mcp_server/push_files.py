async def push_files(
    session,
    branch: str,
    files: list,
    message: str,
    owner: str,
    repo: str,
    output_file: str | None = None,
):
    """
    Push multiple files to a GitHub repository in a single commit

    Args:
        session: MCP ClientSession from connect().
        branch (str): Branch to push to
        files (list): Array of file objects to push, each object with path (string) and content (string)
        message (str): Commit message
        owner (str): Repository owner
        repo (str): Repository name
        output_file (str | None): If set, write full result to this file and return a summary.

    Returns:
        str: Tool result text. If output_file is set, returns a short summary instead.
    """
    args = {}
    args["branch"] = branch
    args["files"] = files
    args["message"] = message
    args["owner"] = owner
    args["repo"] = repo

    result = await session.call_tool("push_files", arguments=args)

    # Extract text from result content
    texts = []
    for content in result.content:
        if hasattr(content, "text"):
            texts.append(content.text)
        else:
            texts.append(str(content))
    full_text = "\n".join(texts)

    if output_file:
        with open(output_file, "w") as f:
            f.write(full_text)
        lines = full_text.count("\n")
        return f"Wrote {len(full_text)} chars ({lines} lines) to {output_file}"

    return full_text
